## Return a hash of font data
sub parse_fontdata {
my $txt = shift; 
my @lines = split /^/, $txt;
t "lines of text = ".@lines;

my %d;
my $k = undef;
my @filler;
foreach(@lines){
   chomp;
   s/^\s*//;
   s/\s*$//;
   next unless length;
   # does this look like the start of a new character?
   if(/^\/\*\s+CHARACTER NUMBER\s+(.*)$/){
      my $desc = $1;
      $desc =~ s/\s*\*\/\s*$//;
      # save the current list to the previous character
      if($k){
         push @{$d{$k}}, @filler;
      }
      @filler = ();
      $k = $desc;
      # t $k;
      $d{$k} = [];
   } else {
      next unless defined $k;
      push @filler, $_;
    
   } 
}
if($k){
   push @{$d{$k}}, @filler;
   #t "last key was $k";
}
return %d;
}


